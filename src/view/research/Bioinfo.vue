      <h3>一. 基于模拟集群分析和机器学习技术的高效化学信息学方法研究</h3>
      <p>
        高质量的化学信息学模型是解决化学和生物医药问题的重要途径，但其构建过程中需综合考虑诸多化学建模的基础问题，如数据模式分析、奇异样本检测、冗余特征删除、非线性建模、模型应用域评估、多目标处理以及多种信息融合等。
        <span class="underline">
          为解决结构复杂数据的模型构建及评价问题，申请人发展了基于模型集群分析理论的化学建模及模型评价新方法和基于协同过滤推荐及核融合技术的多尺度药物信息融合建模新算法。
        </span>
      </p>

      <p>
        <span class="bold"> ① 模型集群分析策略 </span>
        ：在化学或生物学研究中，我们感兴趣的是如何发现仪器测量数据中蕴含的化学或生物学规律，并在此基础上进一步获取该测量体系的一些更有价值的信息，如样本中奇异值或异常点、组学中的生物标记物或生物标记模式、仪器谱学测量中各种响应变量的主要特征、模型的数学表达以及应用范畴等。针对上述化学建模基础问题，
        <span class="underline">
          申请人创造性地提出了模型集群分析（MPA）的策略
        </span>
        ，该方法是通过采用不同的统计采样技术构建成千上万化学模型，再研究这些模型特征的统计学行为来发展新型化学信息学算法的策略；MPA的主要思路是如何最大限度地有效利用已有样本集的信息，利用不同数据采样技术从不同角度考察数据集的内在性质，继而对所得结果进一步统计分析来获得数据集的内在结构（TrAC-Trends
        Anal. Chem., 2012, 38, 154；J. Chemom., 2010, 24, 418）。
      </p>
      <p>
        奇异点在统计学上被定义为一些远离数据主体的异常数据样本，化学数据中奇异样本的存在会撕毁化学数据的主体分布，进而严重影响后续化学模型的构建和稳健性。目前统计学上一些常规的稳健指标和方法对于多个奇异样本存在的体系，其诊断和检出的效果非常不理想。申请人基于MPA策略，
        <span class="underline">
          首次创新提出基于样本空间的随机采样来实现奇异样本检测的蒙特卡洛新算法
        </span>
        ，通过研究样本在模型种群中预测误差的分布，发现预测误差分布的统计特征（预测误差的均值和方差）能够有效区分正常样本和各种类型奇异样本，并有效降低掩蔽效应和沼泽效应带来的风险。该方法在多个数据集上的奇异样本诊断能力显著优于统计学的各种稳健回归和组合算法，有效解决了多个及多种奇异样本同时存在检测困难的问题（J.
        Comput. Chem., 2010, 31,
        592，他引115次，代表性论文一；专利申请号：202010431470.9）。在该策略的基础上，通过在样本空间、特征空间、算法空间及算法参数空间进行随机统计采样，
        <span class="underline">
          创新发展了子窗口重排分析法、随机蛙跳法、子空间软收缩法、蒙特卡洛树算法及多空间模型共识等算法来实现特征选择、非线性建模、数据模式分析及模型应用域评估等多个化学建模基础问题的研究
        </span>
        ，有效解决了高精度化学信息学模型构建和评价难题（TrAC-Trends Anal. Chem.,
        2012, 40, 158；Brief. Bioinform., 2021, bbaa374；Brief. Bioinform.,
        2020, bbaa321；Anal. Chim. Acta, 2016, 908,
        63，扩展ESI高被引论文；Analyst, 2016, 141, 5586；J. Chem. Inf. Model.,
        2019, 59, 3714；Anal. Chim. Acta, 2015, 880, 32）。
      </p>
      <p>
        <span class="bold"> ② 多目标优化与多尺度信息融合算法 </span>
        ：药物分子设计是一个多参数优化和多水平信息整合问题。如药物生物活性需要从生化水平、细胞水平、动物水平、个体水平乃至种群水平上进行测试和评估；分子结构优化改造需同时考虑生物活性、成药性、安全性、选择性及可合成性等多个关键特性。如何实现这种多尺度多参数复杂数据的有效建模分析是化学信息学面临的难点问题。针对结构多样及关系复杂数据建模问题，发展了
        <span class="underline">
          基于协同过滤推荐系统和多核融合的多尺度信息融合系列创新算法
        </span>
