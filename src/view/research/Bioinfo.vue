        2015, 4, 498；Anal. Chim. Acta, 2011, 706, 97；Chemom. Intell. Lab.
        Syst., 2015, 46, 494）；针对多个目标需同时处理的建模问题，
        <span class="underline">
          提出基于帕累托非支配排序思想的多目标处理和优化策略
        </span>
        ，发展了通过加权随机采样方式注入重要模型信息来指导进化的单目标和多目标全局进化算法，并应用于影响分子生物活性和细胞渗透性的重要分子结构特征同时选择研究，解决了分子多个性质同时优化异常困难的问题（Trends
        Anal. Chem., 2019, 113, 102，ESI高被引论文；J. Chem. Inf. Model., 2016,
        56, 763；J. Chemom., 2015, 29,
        627），进一步整合优化开发了EcoFFeS开源软件，实现基于单目标和多目标（NSGA-II和MOEA/D）全局优化的多参数优化及组合不同机器学习技术的关键分子特征选择，
        <span class="underline">
